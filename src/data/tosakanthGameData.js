export function getTosakanthCategoryById(id) {
  return tosakanthCategories.find((category) => category.id === id)
}

export function getRoundClue(round) {
  if (round.hint) {
    return round.hint
  }

  const name =
    round.answer
      .replace(/^(ผศ\.|รศ\.|อ\.|ดร\.|\s)+/g, '')
      .trim() || round.answer

  if (name.length <= 2) {
    return `ชื่อมี ${name.length} ตัวอักษร`
  }

  return `ขึ้นต้นด้วย "${name.charAt(0)}" · ชื่อมี ${name.length} ตัวอักษร`
}

